SELECT	CP.*, T.Text, P.*
FROM		sys.DM_Exec_Cached_Plans CP 
				CROSS APPLY sys.DM_Exec_SQL_Text (CP.Plan_Handle) T
				CROSS APPLY sys.DM_Exec_Query_Plan (CP.Plan_Handle) P
WHERE	T.Text LIKE '%Raz%' 
		AND T.Text NOT LIKE '%DM_Exec_Cached_Plans%'

